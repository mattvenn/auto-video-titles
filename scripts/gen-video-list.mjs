#!/usr/bin/env node
// Scans public/ for .mp4, .mov, and .webm files and writes src/compositions/video-list.ts
// Run automatically via `make studio` and `make render-all`.

import { readdirSync, writeFileSync } from 'fs';
import { join, dirname } from 'path';
import { fileURLToPath } from 'url';

const root = join(dirname(fileURLToPath(import.meta.url)), '..');
const publicDir = join(root, 'public');
const outFile = join(root, 'src', 'compositions', 'video-list.ts');

const files = readdirSync(publicDir)
  .filter(f => f.endsWith('.mp4') || f.endsWith('.mov') || f.endsWith('.webm'))
  .sort();

if (files.length === 0) {
  console.error('gen-video-list: no .mp4, .mov, or .webm files found in public/');
  process.exit(1);
}

const content = `// AUTO-GENERATED by scripts/gen-video-list.mjs — do not edit by hand
// Re-run via \`make studio\` or \`make render-all\` to pick up new videos.
export const BACKGROUND_VIDEOS = ${JSON.stringify(files, null, 2)} as const;
export type BackgroundVideo = (typeof BACKGROUND_VIDEOS)[number];
`;

writeFileSync(outFile, content);
console.log(`gen-video-list: wrote ${files.length} entries to src/compositions/video-list.ts`);
