#!/usr/bin/env node
// Scans public/ for video files and regenerates src/compositions/video-list.ts

import { readdirSync, writeFileSync } from 'fs';
import { join, dirname } from 'path';
import { fileURLToPath } from 'url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const publicDir = join(__dirname, '..', 'public');
const outFile = join(__dirname, '..', 'src', 'compositions', 'video-list.ts');

const VIDEO_EXTS = new Set(['.mp4', '.mov', '.webm']);

const videos = readdirSync(publicDir)
  .filter(f => VIDEO_EXTS.has(f.slice(f.lastIndexOf('.')).toLowerCase()))
  .sort();

const lines = [
  '// AUTO-GENERATED by scripts/gen-video-list.mjs — do not edit by hand',
  '// Re-run via `make studio` or `make render-all` to pick up new videos.',
  `export const BACKGROUND_VIDEOS = [`,
  ...videos.map(v => `  ${JSON.stringify(v)},`),
  `] as const;`,
  `export type BackgroundVideo = (typeof BACKGROUND_VIDEOS)[number];`,
  '',
];

writeFileSync(outFile, lines.join('\n'));
console.log(`Wrote ${videos.length} video(s) to src/compositions/video-list.ts`);
